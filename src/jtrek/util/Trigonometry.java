	/**
	* Gives approximate distance from (x1,y1) to (x2,y2) with only overestimations, and then never by more
	* than (9/8) + one bit uncertainty.
	*/
	public static int ihypot(int x1, int y1) {		
		int x2 = x1 >= 0 ? x1 : -x1;
		int y2 = y1 >= 0 ? y1 : -y1;
		return (x2 + y2 - (((x2 > y2) ? y2 : x2) >> 1));
	}

